public class Arthematic_Operation
{
    public static void main(String args [])
    {
        int a=5, b=9;
        int x = a+b;
        int y = a-b;
        int z = a*b;
        System.out.println("sum = " +x);
        System.out.println("sub = " +y);
        System.out.println("mul = " +z);
    }
}