public class Arthematic_Operation
{
    public static void main(String args [])
    {
        int a=5, b=9;
        int x = a+b;
        int y = a-b;
        int z = a*b;
        System.out.println("sum of 2 numbers = " +x);
        System.out.println("differance of 2 numbers = " +y);
        System.out.println("product of 2 numbers = " +z);
    }
}